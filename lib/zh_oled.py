def draw_missing(oled, x, y):
    oled.rect(x, y, FONT_W, FONT_H, 1)
    oled.line(x, y, x + FONT_W - 1, y + FONT_H - 1, 1)
    oled.line(x + FONT_W - 1, y, x, y + FONT_H - 1, 1)


def draw_zh_char(oled, ch, x, y):
    bitmap = ZH_FONT.get(ch)
    if bitmap is None:
        draw_missing(oled, x, y)
        return FONT_W + FONT_GAP

    for row, bits in enumerate(bitmap):
        for col, bit in enumerate(bits):
            if bit == "1":
                oled.pixel(x + col, y + row, 1)
    return FONT_W + FONT_GAP


def draw_text(oled, text, x, y):
    start_x = x
    for ch in str(text):
        if ch == "\n":
            y += FONT_H + FONT_GAP
            x = start_x
        elif ord(ch) < 128:
            oled.text(ch, x, y + 2)
            x += ASCII_W
        else:
            x += draw_zh_char(oled, ch, x, y)
        if x > 120:
            break


def draw_lines(oled, lines):
    oled.fill(0)
    for row, line in enumerate(lines[:5]):
        draw_text(oled, line, 0, row * (FONT_H + FONT_GAP))
    oled.show()
